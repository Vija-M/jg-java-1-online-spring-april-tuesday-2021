class ArrayLength3 {
    public static void main(String[] args) {
        int[] numbers = new int[2];
        numbers[0] = 10;
        numbers[1] = 20;
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
}
